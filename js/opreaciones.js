// suma
let numero_uno = 87;
let numero_dos = 17;

//calculo o procesos
let resultado_suma = numero_uno + numero_dos;
let resultado_resta = numero_uno - numero_dos;
let resultado_multiplicacion = numero_uno * numero_dos;

// salir por console
console.log("LA SUMA ES: " + resultado_suma);
console.log("LA RESTA ES: " + resultado_resta);
console.log("LA MULTIPLICACION ES: " + resultado_multiplicacion);

// division
let resultado_division = numero_uno / numero_dos;

// residio division
let resultado_residuo = numero_uno % numero_dos;

console.log("LA DIVISION ES: " + resultado_division);
console.log("EL RESIDUO DE LA DIVISION ES: " + resultado_residuo);
